                <h2>Details</h2>
                Автономная работа в течение целого дня
Смартфон оснащен мощным аккумулятором 4000 мА*ч и будет идеально соответствовать вашему активному образу жизни, будь вы любитель социальных сетей, заядлый геймер или просто любитель общения.

Защитное покрытие от брызг
Vibe P1m идеально подходит для тех, кто ведет активный образ жизни. Водонепроницаемое нано-покрытие защищает телефон от брызг и пролития жидкостей, что значительно повышает надежность устройства. Водостойкий слой имеет впечатляющую водоотталкивающую способность, не оставляет никаких шансов для проникновения влажности и коррозии.

Быстрая подзарядка и энергосберегающие функции
Благодаря технологии Quick Charge, 15-минутная подзарядка обеспечит вашему Vibe P1m до 4 ч работы в режиме разговора, а включив функцию энергосбережения OneKey Power Saver, вы можете увеличить вдвое срок службы аккумулятора.

Яркий дисплей
Яркий 5-дюймовый IPS-дисплей стандарта (1280 x 720) превращает игры, просмотр видео или изображений в настоящее удовольствие. К тому же, технология IPS гарантирует почти 180-градусный угол обзора, поэтому дисплей Vibe P1m идеально подходит для совместного просмотра контента с друзьями.

Технология USB On-the-Go
Позволяет заряжать другие устройства, подключив их к P1m с помощью кабеля USB On-The-Go, а также делиться данными с другими ПК, планшетами и смартфонами.

ОС Android 5.1 Lollipop
Благодаря новым функциям, радикальным визуальным изменениям и многочисленным системным улучшением, операционная система Android Lollipop работает еще быстрее и эффективнее, а также потребляет меньше заряд аккумулятора по сравнению с прошлыми версиями. ОС также прекрасно работает со всеми вашими любимыми приложениями от Google.

Молниеносная скорость сетей 4G
Смартфон Vibe P1m поддерживает высокоскоростные сети LTE (4G), гарантируя быструю передачу данных для комфортного веб-браузинга, работы с приложениями и игр в онлайн-режиме.

Четкие фото
Делайте великолепные снимки на ваш P1m как днем, так и ночью, благодаря основной камере 8 Мп и наслаждайтесь качественными видео-чатами с фронтальной камерой 5 Мп.

Возможность расширения хранилища данных
Внутреннюю память P1m можно с легкостью увеличить на дополнительные 32 ГБ, установив карту microSD: теперь вам всегда хватит места для фотографий, музыки, видео и других файлов.

Встроенные приложения
Приложения DOit от Lenovo расширяют возможности устройства. Приложение SHAREit с CLONEit позволяет передавать данные на другие устройства без подключения к сети или соединения WiFi. А такие приложения как Evernote и Kingsoft Office позволят вам оставаться продуктивными, даже если приходится работать на ходу.

Исключительная производительность
Vibe P1m оснащен четырехъядерным 64-битным процессором MediaTek с тактовой частотой 1 ГГц, который обеспечивает идеальный баланс производительности и мощности. А 2 ГБ оперативной памяти гарантируют Vibe P1m быструю передачу данных и бесперебойную работу современной ОС Android.

Поддержка двух SIM-карт
Смартфон поддерживает две SIM-карты: вы сможете с легкостью разделить рабочее и личное общение, сэкономить на разговорах в зарубежной поездке или просто воспользоваться наиболее выгодным тарифом.
