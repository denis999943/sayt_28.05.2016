Высокопроизводительный процессор
Huawei Р8 является первым смартфоном, оснащенным 8-ядерным процессором Hisilicon Kirin 930, обеспечивающим наилучшее соотношение производительности и энергопотребления. Huawei Р8 идеально подойдет как для бизнеса, так и для развлечений, любимых 3D-игр и просмотра фильмов.

Новые возможности камеры
Инновационная основная камера 13 Мп обладает первым в мире четырехцветным датчиком изображения (RGBW) и процессором изображений, аналоги которого устанавливаются в зеркальные фотокамеры.

