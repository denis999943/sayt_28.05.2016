Помимо этого, Huawei Р8 обладает лучшей в своем классе оптической стабилизацией изображения, которая компенсирует сдвиги до 1.2°. Всем этим управляет фирменное программное обеспечение, которое обеспечивает высокое качество съемки как фото, так и видео.

Качественный звук
Высокое качество аудио достигается благодаря программной обработке и технологии шумоподавления. Чувствительный микрофон и встроенная аудиосистема позволяют записывать аудио на большом расстоянии. В громких местах можно увеличить звук до 58 процентов выше нормального уровня, а в ветреную погоду Huawei Р8 может устранить 90% шума ветра при использовании гарнитуры.

Стабильное соединение
Huawei Р8 поддерживает все современные стандарты связи, включая 4G LTE. Умное переключение между двумя антеннами обеспечивает устойчивый сигнал сети и надежность приема и передачи данных.

