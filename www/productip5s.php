                <h2>Details</h2>
                <div class="std"> Незаменим с момента создания

Процессор A7 с 64-битной архитектурой. Датчик идентификации по отпечатку пальца. Усовершенствованная камера, которая стала ещё быстрее. Операционная система, разработанная специально для 64-битной архитектуры. Любая из этих функций позволила бы смартфону быть на шаг впереди других. А со всеми этими функциями iPhone просто опережает своё время.

Ваш отпечаток пальца

Новый датчик идентификации по отпечатку пальца Touch ID развивает идею использования прикосновений. Ваш отпечаток пальца — это самый надёжный пароль. Он всегда с вами и такой пароль никто не сможет подобрать. А ещё нам кажется совершенно естественным, чтобы телефон сам узнавал своего владельца. Он должен знать вас, а не вы должны запоминать и вводить пароль, чтобы его использовать. И самым естественным местом расположения сенсора мы посчитали кнопку «Домой», ведь именно с неё начинается ваш контакт с iPhone.

Команде специалистов биометрии и инженеров аппаратного обеспечения удалось переосмыслить технологию работы сенсора и изменить традиционную кнопку «Домой». Поверхность кнопки вырезана из сапфирового стекла лазером. Изображение пальца попадает на ёмкостный сенсор, который распознает его рисунок, позволяя получить детальный отпечаток. Кнопку окружает кольцо из нержавеющей стали, которое определяет положение пальца, активирует сенсор и улучшает коэффициент сигнал/шум. Затем программа считывает ваш отпечаток и находит соответствие, позволяя разблокировать телефон. Эта совершенная технология не влияет на работу пользователя. Вы ничего не заметите. Кроме того, что разблокировать iPhone стало ещё проще.

Производительность выросла без ущерба для аккумулятора

Заметно повысить производительность устройства и реалистичность графики — вот основные задачи, которые ставят перед собой разработчики мобильных процессоров. Но за счёт производительности не должна страдать энергоэффективность. Создатели iPhone никогда не жертвовали временем работы от аккумулятора ради скорости. Поэтому для iPhone 5s были разработаны совершенно новые процессор и сопроцессор: A7 и M7.

Процессор A7 имеет 64-битную архитектуру, впервые применённую в смартфоне. Для перехода к 64-битной архитектуре потребовались годы непрерывной работы. Но мы пошли на это, потому что хотели поместить на вашу ладонь вычислительную мощь настольного компьютера. Принять решение о развитии этого направления было очень просто. Достаточно было представить, какие возможности это откроет перед пользователями. Благодаря процессору A7, iPhone 5s обладает достаточной мощностью для своих новых возможностей. Скорость графики и общая производительность процессора в два раза больше, чем у предыдущего поколения. В нëм используется новая технология обработки сигнала изображения. Он поддерживает OpenGL ES версии 3.0, позволяя добиваться визуальных эффектов, которые до этого были возможны только на компьютерах и игровых консолях. И несмотря на улучшенную производительность, процессор A7 невероятно энергоэффективен.

Благодаря новому сопроцессору M7, iPhone 5s работает ещё более эффективно. Он разгружает процессор A7, обрабатывая сигналы движения акселерометра, гироскопа и компаса. Приложения используют эти данные, не загружая процессор A7. А поскольку сопроцессор M7 был создан специально для этих задач, ему требуется значительно меньше энергии, чем понадобилось бы A7. Таким образом, заряд аккумулятора расходуется более экономно.

Самая популярная в мире камера

Чтобы стать незаменимой, камера смартфона должна достичь определённого уровня. Она всегда должна обеспечивать высокое качество снимков. Иметь широкие возможности и быть простой в использовании. И помещаться в компактном и лёгком устройстве, которое всегда будет с вами. Камера iSight очень хорошо справляется с этими задачами. Настолько хорошо, что она стала самой популярной в мире камерой. Но несмотря на эту популярность, мы хотели сделать её ещё лучше. Решив улучшить камеру, мы могли пойти двумя путями. Увеличить количество мегапикселей и сыграть на этом при продажах. Или оставить 8‑мегапиксельную матрицу, снабдив её ультрасовременным увеличенным сенсором.

С увеличением сенсора увеличился и размер каждого пикселя. А ведь известно, что на качество изображения влияет именно размер пикселей, а не их количество. Поэтому iPhone 5s оснащён новым 8-мегапиксельным сенсором. Он больше предыдущего на 15%, а размер каждого пикселя составляет 1.5 мкм. Значение диафрагмы теперь составляет ƒ/2.2. В результате светочувствительность камеры возросла на 33%. А это очень важно, поскольку от количества проходящего света напрямую зависит качество, яркость и даже точность цветов изображения. В свою очередь новая вспышка True Tone автоматически определяет, какое именно количество света нужно для каждого снимка. Зачем мы добавили все эти функции в и без того замечательную камеру? Потому что гораздо эффективнее научить iPhone снимать по-настоящему хорошие кадры, чем объяснять тонкости фотосъёмки миллионам пользователей.

Уникальная система. Неповторимый телефон.

iOS 9 — это самая совершенная, многофункциональная и безопасная операционная система для мобильных устройств во всём мире. Она становится ещё более удобной и незаменимой благодаря новым встроенным приложениям, расширенным возможностям Siri и другим функциям. Тесная интеграция с аппаратным обеспечением Apple гарантирует превосходную совместную работу оборудования и ПО. А в App Store есть множество замечательных приложений, способных в полной мере использовать широкие возможности iPhone.
